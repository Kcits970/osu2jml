package jml;

public class EmptyThresholdSet {
    double filler;
    double holdThreshold;
    double resetThreshold;

    public EmptyThresholdSet(double filler, double holdThreshold, double resetThreshold) {
        this.filler = filler;
        this.holdThreshold = holdThreshold;
        this.resetThreshold = resetThreshold;
    }
}
